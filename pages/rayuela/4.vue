        papas fritas acababan casi siempre en el pelo de los tipos de la otra
        mesa, y había que disculparse o decirle a la Maga que era una
        inconsciente. Dentro del grupo la Maga funcionaba muy mal, Oliveira se
        daba cuenta de que prefería ver por separado a todos los del Club, irse
        por la calle con Etienne o con Babs, meterlos en su mundo sin pretender
        nunca meterlos en su mundo pero metiéndolos porque era gente que no
        estaba esperando otra cosa que salirse del recorrido ordinario de los
