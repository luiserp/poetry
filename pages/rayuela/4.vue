        los signos de la noche, acatando itinerarios nacidos de una frase de
        clochard, de una bohardilla iluminada en el fondo de una calle negra,
        deteniéndose en las placitas confidenciales para besarse en los bancos o
        mirar las rayuelas, los ritos infantiles del guijarro y el salto sobre
        un pie para entrar en el Cielo. La Maga hablaba de sus amigas de
        Montevideo, de años de infancia, de un tal Ledesma, de su padre.
        Oliveira escuchaba sin ganas, lamentando un poco no poder interesarse;
        Montevideo era lo mismo que Buenos Aires y él necesitaba consolidar una
        ruptura precaria (¿qué estaría haciendo Traveler, ese gran vago, en qué
        líos majestuosos se habría metido desde su partida? Y la pobre boba de
        Gekrepten, y los cafés del centro), por eso escuchaba displicente y
        hacía dibujos en el pedregullo con una ramita mientras la Maga explicaba
        por qué Chempe y Graciela eran buenas chicas, y cuánto le había dolido
        que Luciana no fuera a despedirla al barco, Luciana era una snob, eso no
        lo podía aguantar en nadie.
        <br />
        —¿Qué entendés por snob? —preguntó Oliveira, más interesado.
        <br />
        —Bueno —dijo la Maga, agachando la cabeza con el aire de quien presiente
        que va a decir una burrada— yo me vine en tercera clase, pero creo que
        si hubiera venido en segunda Luciana hubiera ido a despedirme.
        <br />
        —La mejor definición que he oído nunca —dijo Oliveira.
        <br />
        —Y además estaba Rocamadour —dijo la Maga.
        <br />
        Así fue como Oliveira se enteró de la existencia de Rocamadour, que en
        Montevideo se llamaba modestamente Carlos Francisco. La Maga no parecía
        dispuesta a proporcionar demasiados detalles sobre la génesis de
        Rocamadour, aparte de que se había negado a un aborto y ahora empezaba a
        lamentarlo.
        <br />
        —Pero en el fondo no lo lamento, el problema es cómo voy a vivir, Madame
        Irène me cobra mucho, tengo que tomar lecciones de canto, todo eso
        cuesta.
        <br />
        La Maga no sabía demasiado bien por qué había venido a París, y Oliveira
        se fue dando cuenta de que con una ligera confusión en materia de
        pasajes, agencias de turismo y visados, lo mismo hubiera podido recalar
        en Singapur que en Ciudad del Cabo; lo único importante era haber salido
        de Montevideo, ponerse frente a frente con eso que ella llamaba
        modestamente «la vida». La gran ventaja de París era que sabía bastante
        francés (more Pitman) y que se podían ver los mejores cuadros, las
        mejores películas, la Kultur en sus formas más preclaras.
        <br />
        A Oliveira lo enternecía este panorama (aunque Rocamadour había sido un
        sosegate bastante desagradable, no sabía por qué), y pensaba en algunas
        de sus brillantes amigas de Buenos Aires, incapaces de ir más allá de
        Mar del Plata a pesar de tantas metafísicas ansiedades de experiencia
        planetaria. Esta mocosa, con un hijo en los brazos para colmo, se metía
        en una tercera de barco y se largaba a estudiar canto a París sin un
        vintén en el bolsillo. Por si fuera poco ya le daba lecciones sobre la
        manera de mirar y de ver; lecciones que ella no sospechaba, solamente su
        manera de pararse de golpe en la calle para espiar un zaguán donde no
        había nada, pero más allá un vislumbre verde, un resplandor, y entonces
        colarse furtivamente para que la portera no se enojara, asomarse al gran
        patio con a veces una vieja estatua o un brocal con hiedra, o nada,
        solamente el gastado pavimento de redondos adoquines, verdín en las
        paredes, una muestra de relojero, un viejito tomando sombra en un
        rincón, y los gatos, siempre inevitablemente los minouche morrongos
        miaumiau kitten kat chat cat gatoo grises y blancos y negros y de
        albañal, dueños del tiempo y de las baldosas tibias, invariables amigos
