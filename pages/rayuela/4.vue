        lenguaje entre tonto y misterioso, con citas a plazo fijo, consejos y
        advertencias. De golpe Oliveira se extrañaba andando con la Maga, de
        nada le servía irritarse porque a la Maga se le volcaban casi siempre
        los vasos de cerveza o sacaba el pie de debajo de una mesa justo para
        que el mozo tropezara y se pusiera a maldecir; era feliz a pesar de
        estar todo el tiempo exasperado por esa manera de no hacer las cosas
        como hay que hacerlas, de ignorar resueltamente las grandes cifras de la
        cuenta y quedarse en cambio arrobada delante de la cola de un modesto 3,
