        parada como si tal cosa para mirar desde el medio de la calle una vista
        del Panteón a lo lejos, siempre mucho mejor que la vista que se tenía
        desde la vereda. Y cosas por el estilo.
        <br />
        Oliveira ya conocía a Perico y a Ronald. La Maga le presentó a Etienne y
        Etienne les hizo conocer a Gregorovius; el Club de la Serpiente se fue
        formando en las noches de Saint-Germain-des-Prés. Todo el mundo aceptaba
        en seguida a la Maga como una presencia inevitable y natural, aunque se
        irritaran por tener que explicarle casi todo lo que se estaba hablando,
