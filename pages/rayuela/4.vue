        ocurriera lo que tenía que ocurrir, queriéndose y peleándose y todo esto
        al margen de las noticias de los diarios, de las obligaciones de familia
        y de cualquier forma de gravamen fiscal o moral.
        <br />
        Toc, toc.
        <br />
        —Despertémonos —decía Oliveira alguna que otra vez.
        <br />
        —Para qué —contestaba la Maga, mirando correr las péniches desde el Pont
        Neuf—. Toc, toc, tenés un pajarito en la cabeza. Toc, toc, te picotea
