        anciano agobiado hace miles de fichas y sabe todo lo que puede saberse
        sobre historiografía. Ibas allí a jugar con un gato, y el viejo te
        dejaba entrar y no te hacía preguntas, contento de que á veces le
        alcanzaras algún libro de los estantes más altos. Y te calentabas en su
        estufa de gran caño negro y no te gustaba que yo supiera que ibas a
        ponerte al lado de esa estufa. Pero todo esto había que decirlo en su
        momento, sólo que era difícil precisar el momento de una cosa, y aún
