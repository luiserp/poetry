        tan mentira era verdadero, un Figari con violetas de anochecer, con
        caras lívidas, con hambre y golpes en los rincones. Más tarde te creí,
        más tarde hubo razones, hubo madame Léonie que mirándome la mano que
        había dormido con tus senos me repitió casi tus mismas palabras. «Ella
        sufre en alguna parte. Siempre ha sufrido. Es muy alegre, adora el
        amarillo, su pájaro es el mirlo, su hora la noche, su puente el Pont des
        Arts.» (Una pinaza color borravino, Maga, y por qué no nos habremos ido
        en ella cuando todavía era tiempo.)
        <br />
