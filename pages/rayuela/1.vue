        prestado, haciendo lo que otros hacen y viendo lo que otros ven. Sé que
        salías de un café de la rue du Cherche-Midi y que nos hablamos. Esa
        tarde todo anduvo mal, porque mis costumbres argentinas me prohibían
        cruzar continuamente de una vereda a otra para mirar las cosas más
        insignificantes en las vitrinas apenas iluminadas de unas calles que ya
        no recuerdo. Entonces te seguía de mala gana, encontrándote petulante y
        malcriada, hasta que te cansaste de no estar cansada y nos metimos en un
        café del Boul’Mich’ y de golpe, entre dos medialunas, me contaste un
