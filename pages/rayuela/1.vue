        hermosísima como una gran cucaracha reluciente de limpieza, con una
        mujer de delantal blanco que colgaba ropa en un alambre de la proa,
        mirando sus ventanillas pintadas de verde con cortinas Hansel y Gretel,
        aún ahora, Maga, me preguntaba si este rodeo tenía sentido, ya que para
        llegar a la rue des Lombards me hubiera convenido más cruzar el Pont
        Saint-Michel y el Pont au Change. Pero si hubieras estado ahí esa noche,
        como tantas otras veces, yo habría sabido que el rodeo tenía un sentido,
