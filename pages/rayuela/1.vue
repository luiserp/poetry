        desencontrarnos minuciosamente. Como no sabías disimular me di cuenta en
        seguida de que para verte como yo quería era necesario empezar por
        cerrar los ojos, y entonces primero cosas como estrellas amarillas
        (moviéndose en una jalea de terciopelo), luego saltos rojos del humor y
