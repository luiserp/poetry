        juntar un lápiz o un trocito de papel que se me han ido de la mano, como
        la noche del terrón de azúcar en el restaurante de la rue Scribe, un
        restaurante bacán con montones de gerentes, putas de zorros plateados y
        matrimonios bien organizados. Estábamos con Ronald y Etienne, y a mí se
        me cayó un terrón de azúcar que fue a parar abajo de una mesa bastante
        lejos de la nuestra. Lo primero que me llamó la atención fue la forma en
        que el terrón se había alejado, porque en general los terrones de azúcar
        se plantan apenas tocan el suelo por razones paralelepípedas evidentes.
        Pero éste se conducía como si fuera una bola de naftalina, lo cual
        aumentó mi aprensión, y llegué a creer que realmente me lo habían
        arrancado de la mano. Ronald, que me conoce, miró hacia donde había ido
        a parar el terrón y se empezó a reír. Eso me dio todavía más miedo,
        mezclado con rabia. Un mozo se acercó pensando que se me había caído
        algo precioso, una Párker o una dentadura postiza, y en realidad lo
        único que hacía era molestarme, entonces sin pedir permiso me tiré al
        suelo y empecé a buscar el terrón entre los zapatos de la gente que
        estaba llena de curiosidad creyendo (y con razón) que se trataba de algo
        importante. En la mesa había una gorda pelirroja, otra menos gorda pero
        igualmente putona, y dos gerentes o algo así. Lo primero que hice fue
        darme cuenta de que el terrón no estaba a la vista y eso que lo había
        visto saltar hasta los zapatos (que se movían inquietos como gallinas).
        Para peor el piso tenía alfombra, y aunque estaba asquerosa de usada el
        terrón se había escondido entre los pelos y no podía encontrarlo. El
        mozo se tiró del otro lado de la mesa, y ya éramos dos cuadrúpedos
        moviéndonos entre los zapatos gallina que allá arriba empezaban a
        cacarear como locas. El mozo seguía convencido de la Párker o el luis de
        oro, y cuando estábamos bien metidos debajo de la mesa, en una especie
        de gran intimidad y penumbra y él me preguntó y yo le dije, puso una
        cara que era como para pulverizarla con un fijador, pero yo no tenía
        ganas de reír, el miedo me hacía una doble llave en la boca del estómago
        y al final me dio una verdadera desesperación (el mozo se había
        levantado furioso) y empecé a agarrar los zapatos de las mujeres y a
        mirar si debajo del arco de la suela no estaría agazapado el azúcar, y
        las gallinas cacareaban, los gallos gerentes me picoteaban el lomo, oía
        las carcajadas de Ronald y de Etienne mientras me movía de una mesa a
        otra hasta encontrar el azúcar escondido detrás de una pata Segundo
        Imperio. Y todo el mundo enfurecido, hasta yo con el azúcar apretado en
        la palma de la mano y sintiendo cómo se mezclaba con el sudor de la
        piel, cómo asquerosamente se deshacía en una especie de venganza
        pegajosa, esa clase de episodios todos los días.
