        Léonie te mirara la palma de la mano, a lo mejor tuve miedo de que
        leyera en tu mano alguna verdad sobre mí, porque fuiste siempre un
        espejo terrible, una espantosa máquina de repeticiones, y lo que
        llamamos amarnos fue quizá que yo estaba de pie delante de vos, con una
        flor amarilla en la mano, y vos sostenías dos velas verdes y el tiempo
        soplaba contra nuestras caras una lenta lluvia de renuncias y despedidas
